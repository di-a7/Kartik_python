# Membership Operator: used to check if a value or data exists in sequential datatype(group datatype + string datatype)
# output in Boolean datatype
# in : if a data or value exists in a sequential datatype, Output: True, otherwise False 
seq = "member"
print('z' in seq)

my_list = ['membership','operator','used','check','value',15]
print(15 in my_list)



# not in: if a data or value exists in a sequential datatype, Output: False, otherwise True
my_list = ['membership','operator','used','check','value']
print(15 not in my_list)