# Assignment operator: used to assign values to the variables

# equal (=)
a = 10

# add and assign (+=)
a += 5      # a = a + 5

# subtract and assign (-=)
a -= 5      # a = a - 5

# multiply and assign (*=)
a *= 5      # a = a * 5

# divide and assign (/=)
a /= 5      # a = a / 5

# //=
a //= 5      # a = a // 5

# %=
a %= 5      # a = a % 5

# **=
a **= 5      # a = a ** 5
