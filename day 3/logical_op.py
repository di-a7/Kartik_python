# Logical Operator:
# AND: if all the conditions are true, Output : TRUE, otherwise FALSE
# a = True
# b = True
# c = False
print((100 < 50) and (10 == 10) )

# OR: if any one of the conditions are true, OUPUT: TRUE, otherise FALSE
# print( a or b )
print((100 < 50) or (10 == 10) )


# NOT: if condition is true, output: FALSE and vise versa
# print(not b)
print( not (10 == 10) )
