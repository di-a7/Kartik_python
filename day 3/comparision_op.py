# Comparision operator: to compare values, Output: boolean datatype(T/F)
a = 10
b = 10

# greater (>)
print(a > b)   # if the value of a is greater then b True, otherwise False

# less (<)
print(a < b)   # if the value of a is less then b True, otherwise False

# equal to (==)
print(a == b)  # if both values are equal True, otherwise False

# not equal (!=)
print( a != b ) # if both values are equal False, otherwise True

# greater than equal (>=) : if the value of a is greater then and equal to b True, otherwise False


# less than equal (<=) : if the value of a is less then and equal to b True, otherwise False

